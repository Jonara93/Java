package be.technifutur.java2020.projetaidemem;

public class Affichage {
	public static void main(String[] args){
		
		System.out.println("150 - 92 = " + (150-92));
		System.out.println("(89 - 18) + (25*2) = " + (89 - 18) + (25*2));
		System.out.println("(15*3)/2 = " + ((15*3)/2));
		System.out.println("(15+3)/2 = " + ((15+3)/2));
		System.out.println("15/(2*3) = " + 15/(3*2));
		System.out.println("(15/2)+3 = " + (15/2)+3);		
		
	} //end main
}//end class